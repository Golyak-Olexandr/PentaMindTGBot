from aiogram import Router, types
from aiogram.filters import CommandStart, Command

router = Router()

@router.message(CommandStart())
async def cmd_start(message: types.Message):
    start_text = (
        f"👋 <b>Вітаю, {message.from_user.first_name}!</b>\n\n"
        f"🤖 <b>Я — ваш інтелектуальний помічник з планування виробництва.</b>\n\n"
        f"Усередині мене працює <b> ШІ-агент</b>, який на основі прорахованих даних виносить вердикт у звіт. "
        f"Процес обробки займає <b>близько 60 секунд</b> та відбувається у такому порядку:\n\n"
        f"• 📦 <i>Залишки продукції на складах</i>\n"
        f"• 🏭 <i>Статус незавершеного виробництва (НЗВ)</i>\n"
        f"• 🧱 <i>Точну потребу в сировині</i>\n"
        f"• ⏱ <i>Завантаження обладнання в годинах</i>\n"
        f"• ⚖️ <i>Фінальний вердикт щодо можливості виконання</i>\n\n"
        f"━━━━━━━━━━━━━━━━━━━━\n"
        f"📥 <b>Для початку аналізу просто надішліть мені .xlsx-файл із замовленнями.</b>\n\n"
        f"📍 <i>Доступні команди: /start, /help, /admin</i>"
    )
    
    await message.answer(start_text, parse_mode="HTML")

